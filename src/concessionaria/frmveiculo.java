    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Object[] opcao = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null,
                "Tem certeza que deseja excluir?", "ATENÇÃO",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                opcao, opcao[0]);
        if (i == JOptionPane.YES_OPTION) {
            if (txt1.getText().length() > 0) {
                try {
                    String consulta = "delete from veiculo where id_veiculo = " + this.txt1.getText();
                    PreparedStatement pst = banco.con.prepareStatement(consulta);
                    int result = pst.executeUpdate(consulta);
                    if (result > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Veículo deletado com sucesso");
                        txt1.setText("");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Veículo não encontrado");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Problemas ao deletar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Campo 'ID veículo' vázio");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

