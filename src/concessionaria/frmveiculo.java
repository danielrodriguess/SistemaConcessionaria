    private void btncadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastroActionPerformed
        // TODO add your handling code here:
        Object[] opcao = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null,
                "Tem certeza que deseja cadastrar?", "ATENÇÃO",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                opcao, opcao[0]);
        if (i == JOptionPane.YES_OPTION) {
            if (txt1.getText().length() > 0 && txt2.getText().length() > 0 && txt3.getText().length() > 0
                    && txt4.getText().length() > 0 && txt5.getText().length() > 0) {
                double valor_unitario = Double.parseDouble(txt5.getText());
                String status = "Dísponivel";
                try {
                    PreparedStatement pst = banco.con.prepareStatement("insert into veiculo (id_veiculo,placa,modelo,ano_fabricacao,valor_veiculo,status) values (?,?,?,?,?,?)");
                    pst.setString(1, txt1.getText());
                    pst.setString(2, txt2.getText());
                    pst.setString(3, txt3.getText());
                    pst.setString(4, txt4.getText());
                    pst.setDouble(5, valor_unitario);
                    pst.setString(6, status);
                    pst.executeUpdate();
                    txt1.setText("");
                    txt2.setText("");
                    txt3.setText("");
                    txt4.setText("");
                    txt5.setText("");
                    JOptionPane.showMessageDialog(rootPane, "Veículo cadastrado com sucesso");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Não foi cadastrado. Tente mais tarde");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Campos vázios", "Atenção", 2);
            }
        }
    }//GEN-LAST:event_btncadastroActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Object[] opcao = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null,
                "Tem certeza que deseja alterar?", "ATENÇÃO",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                opcao, opcao[0]);
        if (i == JOptionPane.YES_OPTION) {
            if (txt1.getText().length() > 0 && txt2.getText().length() > 0 && txt3.getText().length() > 0
                    && txt4.getText().length() > 0 && txt5.getText().length() > 0) {
                try {
                    PreparedStatement pst = banco.con.prepareStatement("update veiculo set placa = ?,modelo = ?, ano_fabricacao = ?, valor_veiculo = ? where id_veiculo = '" + this.txt1.getText() + "'");
                    pst.setString(1, txt2.getText());
                    pst.setString(2, txt3.getText());
                    pst.setString(3, txt4.getText());
                    pst.setString(4, txt5.getText());
                    int resultado = pst.executeUpdate();
                    if (resultado > 0) {
                        txt1.setText("");
                        txt2.setText("");
                        txt3.setText("");
                        txt4.setText("");
                        txt5.setText("");
                        JOptionPane.showMessageDialog(rootPane, "Veículo alterado com sucesso");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Veículo não encontrado");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Não foi possível alterar. Tente mais tarde");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Campos vázios");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (txt1.getText().length() > 0) {
            try {
                String consulta = "select * from veiculo where id_veiculo = " + this.txt1.getText();
                PreparedStatement pst = banco.con.prepareStatement(consulta);
                banco.rs = pst.executeQuery(consulta);
                if (banco.rs.next()) {
                    String placa = banco.rs.getString("placa");
                    String modelo = banco.rs.getString("modelo");
                    int ano = banco.rs.getInt("ano_fabricacao");
                    double valor = banco.rs.getDouble("valor_veiculo");
                    txt2.setText(placa);
                    txt3.setText(modelo);
                    txt4.setText("" + ano);
                    txt5.setText("" + valor);
                } else {
                    JOptionPane.showMessageDialog(null, "Veículo não encontrado");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível buscar");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Campos vázios");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

